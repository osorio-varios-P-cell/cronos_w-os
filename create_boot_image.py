#!/usr/bin/env python3
import struct

# Mensajes a mostrar
msg1 = b'CRONOS W-OS v2.0 - Sistema Operativo Soberano'
msg2 = b'Exokernel con Grafos - IA Colmena - Crystal UI'
msg3 = b'Seguridad AEGIS - Graph Memory - Genesis Auto-creation'
msg4 = b'Kernel inicializado exitosamente - Sistema listo'

# Crear buffer de 512 bytes
boot_sector = bytearray(512)

# Código de boot simple (en bytes)
boot_code = bytes([
    0xB8, 0x00, 0xB8, 0x8E, 0xC0, 0x31, 0xFF, 0xBE, 0x3E, 0x00, 0xB9, 0x00, 0x08, 0xB8, 0x20, 0x0F, 0xF3, 0xAB,
    0xBE, 0x3E, 0x00, 0xBF, 0x00, 0x00, 0xE8, 0x0D, 0x00, 0xBE, 0x6A, 0x00, 0xBF, 0xA0, 0x00, 0xE8, 0x0D, 0x00,
    0xBE, 0x96, 0x00, 0xBF, 0x40, 0x01, 0xE8, 0x0D, 0x00, 0xBE, 0xC2, 0x00, 0xBF, 0xE0, 0x01, 0xE8, 0x0D, 0x00,
    0xEB, 0xFE, 0xAC, 0x3C, 0x00, 0x74, 0x05, 0xAA, 0xB0, 0x0F, 0xAA, 0xEB, 0xF4, 0xC3
])

# Copiar código de boot
boot_sector[0:len(boot_code)] = boot_code

# Copiar mensajes
boot_sector[0x3E:0x3E+len(msg1)] = msg1
boot_sector[0x6A:0x6A+len(msg2)] = msg2
boot_sector[0x96:0x96+len(msg3)] = msg3
boot_sector[0xC2:0xC2+len(msg4)] = msg4

# Firma de boot (0xAA55)
boot_sector[510] = 0x55
boot_sector[511] = 0xAA

# Crear imagen de disco de 1MB
image = bytearray(1024 * 1024)
image[0:512] = boot_sector

# Escribir imagen
with open('cronos_w-os.img', 'wb') as f:
    f.write(image)

print("Imagen de disco creada: cronos_w-os.img")
